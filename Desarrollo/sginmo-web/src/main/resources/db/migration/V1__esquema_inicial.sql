-- ── Nucleo parametrico ──────────────────────────────────────────────────────

CREATE TABLE parametro_sistema (
  clave        varchar(60) PRIMARY KEY,
  valor        varchar(120) NOT NULL,
  descripcion  varchar(180) NOT NULL,
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz
);

CREATE TABLE entidad (
  entidad      varchar(40)  NOT NULL,
  codigo       varchar(40)  NOT NULL,
  descripcion  varchar(180) NOT NULL,
  valor_texto  varchar(250),
  estado       varchar(10)  NOT NULL DEFAULT 'ACTIVO' CHECK (estado IN ('ACTIVO','INACTIVO')),
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  PRIMARY KEY (entidad, codigo)
);

CREATE TABLE ubicacion_geografica (
  ubicacion_geografica bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  padre        bigint REFERENCES ubicacion_geografica,
  nombre       varchar(150) NOT NULL,
  nivel_lista  varchar(40) NOT NULL DEFAULT 'NIVELES_UBICACION',
  nivel_codigo varchar(40) NOT NULL,
  codigo_oficial varchar(10) UNIQUE,       -- codigo INE (DPA): permite re-importar actualizaciones por upsert
  estado       varchar(10) NOT NULL DEFAULT 'ACTIVO' CHECK (estado IN ('ACTIVO','INACTIVO')),
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  FOREIGN KEY (nivel_lista, nivel_codigo) REFERENCES entidad,
  UNIQUE (padre, nombre, nivel_codigo)
);
CREATE INDEX ix_ubicacion_padre ON ubicacion_geografica (padre);

CREATE TABLE moneda (
  moneda       bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  descripcion  varchar(120) NOT NULL,
  simbolo      varchar(20)  NOT NULL,
  tipo_moneda  varchar(20)  NOT NULL CHECK (tipo_moneda IN ('LOCAL','EXTRANJERA')),
  precision_decimales smallint NOT NULL DEFAULT 0,
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz
);

CREATE TABLE forma_pago (
  forma_pago  bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  descripcion varchar(120) NOT NULL,
  codigo      varchar(20)  NOT NULL UNIQUE,
  dias        int,
  por_defecto boolean NOT NULL DEFAULT false,
  requiere_emisor boolean NOT NULL DEFAULT false,
  requiere_procesador boolean NOT NULL DEFAULT false,
  requiere_numero boolean NOT NULL DEFAULT false,
  requiere_serie boolean NOT NULL DEFAULT false,
  requiere_vencimiento boolean NOT NULL DEFAULT false,
  requiere_cuenta boolean NOT NULL DEFAULT false,
  requiere_referencia boolean NOT NULL DEFAULT false,
  requiere_cobrador boolean NOT NULL DEFAULT false,
  requiere_fecha_deposito boolean NOT NULL DEFAULT false,
  requiere_numero_deposito boolean NOT NULL DEFAULT false,
  requiere_estado_deposito boolean NOT NULL DEFAULT false,
  requiere_motivo_rechazo boolean NOT NULL DEFAULT false,
  requiere_nota_credito boolean NOT NULL DEFAULT false,
  estado varchar(10) NOT NULL DEFAULT 'ACTIVO' CHECK (estado IN ('ACTIVO','INACTIVO')),
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz
);

CREATE TABLE impuesto (
  impuesto     bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  descripcion  varchar(120) NOT NULL,
  porcentaje_impuesto numeric(5,2) NOT NULL,
  factor_discriminado numeric(15,2) NOT NULL,
  factor_impuesto     numeric(15,2) NOT NULL,
  estado varchar(10) NOT NULL DEFAULT 'ACTIVO' CHECK (estado IN ('ACTIVO','INACTIVO')),
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz
);

-- ── Maestro de articulos (productos y servicios; conceptos de dinero) ──────

CREATE TABLE articulo (
  articulo bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  codigo varchar(25) NOT NULL UNIQUE,
  descripcion varchar(150) NOT NULL,
  tipo varchar(10) NOT NULL CHECK (tipo IN ('PRODUCTO','SERVICIO')),
  impuesto bigint NOT NULL REFERENCES impuesto,      -- [Obs 5] el % y factores viven en impuesto
  categoria_lista varchar(40) DEFAULT 'TIPOS_ARTICULO',
  categoria_codigo varchar(40),
  unidad_medida_lista varchar(40) DEFAULT 'UNIDADES_MEDIDA',
  unidad_medida_codigo varchar(40),
  presentacion_lista varchar(40) DEFAULT 'PRESENTACIONES',
  presentacion_codigo varchar(40),
  clasificacion varchar(20),
  marca_lista varchar(40) DEFAULT 'MARCAS',
  marca_codigo varchar(40),
  modelo_lista varchar(40) DEFAULT 'MODELOS',
  modelo_codigo varchar(40),
  familia_lista varchar(40) DEFAULT 'FAMILIAS_ARTICULO',
  familia_codigo varchar(40),
  grupo_lista varchar(40) DEFAULT 'GRUPOS_ARTICULO',
  grupo_codigo varchar(40),
  subgrupo_lista varchar(40) DEFAULT 'SUBGRUPOS_ARTICULO',
  subgrupo_codigo varchar(40),
  procedencia_lista varchar(40) DEFAULT 'PROCEDENCIAS',
  procedencia_codigo varchar(40),
  codigo_barra varchar(50),
  codigo_interno varchar(25),
  precio_unitario numeric(15,2) NOT NULL DEFAULT 0,
  precio_unitario_ext numeric(15,2),
  stock_minimo numeric(15,3),
  stock_maximo numeric(15,3),
  porcentaje_stock_minimo numeric(5,2),
  proveedor bigint,                        -- FK a persona (se agrega tras crear persona)
  ultimo_costo numeric(15,2),
  costo_moneda_ext numeric(15,2),
  proximo_vencimiento date,
  cuenta varchar(50),
  tipo_movimiento varchar(10) CHECK (tipo_movimiento IN ('INGRESO','EGRESO','DESCUENTO')),
  modifica_estado boolean NOT NULL DEFAULT false,
  aplicacion varchar(30) UNIQUE,
  estado varchar(10) NOT NULL DEFAULT 'ACTIVO' CHECK (estado IN ('ACTIVO','INACTIVO')),
  observacion varchar(250),
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  FOREIGN KEY (categoria_lista, categoria_codigo) REFERENCES entidad,
  FOREIGN KEY (unidad_medida_lista, unidad_medida_codigo) REFERENCES entidad,
  FOREIGN KEY (presentacion_lista, presentacion_codigo) REFERENCES entidad,
  FOREIGN KEY (marca_lista, marca_codigo) REFERENCES entidad,
  FOREIGN KEY (modelo_lista, modelo_codigo) REFERENCES entidad,
  FOREIGN KEY (familia_lista, familia_codigo) REFERENCES entidad,
  FOREIGN KEY (grupo_lista, grupo_codigo) REFERENCES entidad,
  FOREIGN KEY (subgrupo_lista, subgrupo_codigo) REFERENCES entidad,
  FOREIGN KEY (procedencia_lista, procedencia_codigo) REFERENCES entidad
);

-- [Obs 6] articulo_detalle eliminada: codigo de barra por variante, color, talle, etc.
-- se cargan como propiedades del articulo (lista PROPIEDADES_ARTICULO: COLOR, TALLE, CODIGO_BARRA_VARIANTE...).
CREATE TABLE articulo_propiedad (
  articulo_propiedad bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  articulo bigint NOT NULL REFERENCES articulo,
  propiedad_lista varchar(40) NOT NULL DEFAULT 'PROPIEDADES_ARTICULO',
  propiedad_codigo varchar(40) NOT NULL,
  valor varchar(100),
  numero numeric(15,2),
  fecha date,
  referencia_lista varchar(40),
  referencia_codigo varchar(40),
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  FOREIGN KEY (propiedad_lista, propiedad_codigo) REFERENCES entidad,
  FOREIGN KEY (referencia_lista, referencia_codigo) REFERENCES entidad,
  UNIQUE (articulo, propiedad_lista, propiedad_codigo)
);

CREATE TABLE articulo_costo (
  articulo_costo bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  articulo bigint NOT NULL REFERENCES articulo,
  fecha date NOT NULL,
  costo numeric(15,2),
  costo_ext numeric(15,2),
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  UNIQUE (articulo, fecha)
);

-- ── Personas: datos basicos + especializacion + roles multiples ────────────
-- [Obs 7 PENDIENTE: definir con el usuario la forma final de la clave persona/fisica/juridica]

CREATE TABLE persona (
  persona bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tipo_personeria varchar(10) NOT NULL CHECK (tipo_personeria IN ('FISICA','JURIDICA')),
  nombre varchar(180) NOT NULL,            -- nombre visible: nombre completo o razon social
  numero_documento varchar(20) NOT NULL UNIQUE,
  digito_verificador char(1),
  tipo_documento_lista varchar(40) DEFAULT 'TIPOS_DOCUMENTOS_IDENTIDAD',
  tipo_documento_codigo varchar(40),
  es_contribuyente boolean NOT NULL DEFAULT false,
  clasificacion_fiscal varchar(20),
  direccion varchar(180), telefono varchar(20), email varchar(180),
  ubicacion bigint REFERENCES ubicacion_geografica,
  ubicacion_url varchar(250),
  estado varchar(10) NOT NULL DEFAULT 'ACTIVO' CHECK (estado IN ('ACTIVO','INACTIVO')),
  observacion text,
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  FOREIGN KEY (tipo_documento_lista, tipo_documento_codigo) REFERENCES entidad
);

CREATE TABLE persona_fisica (
  persona bigint PRIMARY KEY REFERENCES persona,
  nombres varchar(120) NOT NULL,
  apellidos varchar(120) NOT NULL,
  sexo varchar(10) CHECK (sexo IN ('MASCULINO','FEMENINO')),
  fecha_nacimiento date,
  estado_civil_lista varchar(40) DEFAULT 'ESTADOS_CIVILES',
  estado_civil_codigo varchar(40),
  nacionalidad varchar(80),
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  FOREIGN KEY (estado_civil_lista, estado_civil_codigo) REFERENCES entidad
);

CREATE TABLE persona_juridica (
  persona bigint PRIMARY KEY REFERENCES persona,
  razon_social varchar(180) NOT NULL,
  nombre_fantasia varchar(120),
  fecha_constitucion date,
  representante_legal bigint REFERENCES persona,
  actividad_lista varchar(40) DEFAULT 'ACTIVIDADES_ECONOMICAS',
  actividad_codigo varchar(40),
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  FOREIGN KEY (actividad_lista, actividad_codigo) REFERENCES entidad
);

CREATE TABLE persona_rol (
  persona_rol bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  persona bigint NOT NULL REFERENCES persona,
  rol_lista varchar(40) NOT NULL DEFAULT 'ROLES_PERSONA',
  rol_codigo varchar(40) NOT NULL,
  estado varchar(10) NOT NULL DEFAULT 'ACTIVO' CHECK (estado IN ('ACTIVO','INACTIVO')),
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  FOREIGN KEY (rol_lista, rol_codigo) REFERENCES entidad,
  UNIQUE (persona, rol_lista, rol_codigo)
);

ALTER TABLE articulo
  ADD CONSTRAINT fk_articulo_proveedor FOREIGN KEY (proveedor) REFERENCES persona;

-- ── Organizacion ────────────────────────────────────────────────────────────
-- [Obs 8] La tabla empresa fue ELIMINADA: la empresa operadora ES una persona_juridica.

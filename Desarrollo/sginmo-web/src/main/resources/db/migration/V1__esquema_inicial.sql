CREATE TABLE sucursal (                    -- [Obs 9]
  sucursal bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  persona_juridica bigint NOT NULL REFERENCES persona_juridica,
  descripcion varchar(180) NOT NULL,
  direccion varchar(180) NOT NULL, telefono varchar(20) NOT NULL,
  por_defecto boolean NOT NULL DEFAULT false,
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz
);

CREATE TABLE usuario (
  usuario bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  codigo_usuario varchar(20) NOT NULL UNIQUE,
  password_hash varchar(100) NOT NULL,
  perfil varchar(20) NOT NULL CHECK (perfil IN ('ADMINISTRADOR','USUARIO')),
  estado varchar(10) NOT NULL DEFAULT 'ACTIVO' CHECK (estado IN ('ACTIVO','INACTIVO')),  -- [Obs 10]
  empresa bigint NOT NULL REFERENCES persona_juridica,
  persona bigint REFERENCES persona,
  intentos_fallidos smallint NOT NULL DEFAULT 0,
  bloqueado_hasta timestamptz,
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz
);

-- ── Activos: recursivo; el tipo define, los atributos complementan ─────────

CREATE TABLE activo (
  activo bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  padre bigint REFERENCES activo,
  nombre varchar(180) NOT NULL,
  tipo_lista varchar(40) NOT NULL DEFAULT 'TIPOS_ACTIVO',
  tipo_codigo varchar(40) NOT NULL,
  empresa bigint REFERENCES persona_juridica,
  ubicacion bigint REFERENCES ubicacion_geografica,
  direccion varchar(250),
  precio_venta numeric(15,2) NOT NULL DEFAULT 0,
  comision_venta numeric(5,2) NOT NULL DEFAULT 0,
  precio_alquiler numeric(15,2) NOT NULL DEFAULT 0,
  comision_alquiler numeric(5,2) NOT NULL DEFAULT 0,
  estado varchar(20) NOT NULL DEFAULT 'LIBRE' CHECK (estado IN ('LIBRE','OCUPADA','VENDIDA')),
  cuenta_catastral varchar(120), numero_finca varchar(20),
  numero_lote varchar(20), numero_manzana varchar(20),
  observacion varchar(250),
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  FOREIGN KEY (tipo_lista, tipo_codigo) REFERENCES entidad
);
CREATE INDEX ix_activo_padre ON activo (padre);

CREATE TABLE atributo (
  atributo bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  grupo_lista varchar(40) NOT NULL DEFAULT 'GRUPOS_ATRIBUTOS',
  grupo_codigo varchar(40) NOT NULL,
  descripcion varchar(120) NOT NULL,
  tipo_dato varchar(10) NOT NULL CHECK (tipo_dato IN ('TEXTO','NUMERO','BOOLEANO','FECHA')),
  estado varchar(10) NOT NULL DEFAULT 'ACTIVO' CHECK (estado IN ('ACTIVO','INACTIVO')),
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  FOREIGN KEY (grupo_lista, grupo_codigo) REFERENCES entidad,
  UNIQUE (grupo_codigo, descripcion)
);

CREATE TABLE atributo_por_tipo (
  atributo_por_tipo bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  atributo bigint NOT NULL REFERENCES atributo,
  tipo_lista varchar(40) NOT NULL DEFAULT 'TIPOS_ACTIVO',
  tipo_codigo varchar(40) NOT NULL,
  obligatorio boolean NOT NULL DEFAULT false,
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  FOREIGN KEY (tipo_lista, tipo_codigo) REFERENCES entidad,
  UNIQUE (atributo, tipo_lista, tipo_codigo)
);

CREATE TABLE activo_atributo (
  activo_atributo bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  activo bigint NOT NULL REFERENCES activo,
  atributo bigint NOT NULL REFERENCES atributo,
  valor text,
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  UNIQUE (activo, atributo)
);

CREATE TABLE activo_propietario (
  activo_propietario bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  activo bigint NOT NULL REFERENCES activo,
  propietario bigint NOT NULL REFERENCES persona,
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  UNIQUE (activo, propietario)
);

-- ── Operaciones ─────────────────────────────────────────────────────────────

CREATE TABLE operacion (
  operacion bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  fecha_operacion date NOT NULL,
  tipo_operacion varchar(10) NOT NULL CHECK (tipo_operacion IN ('ALQUILER','VENTA')),
  cliente bigint NOT NULL REFERENCES persona,
  vendedor bigint REFERENCES persona,
  activo bigint NOT NULL REFERENCES activo,
  fecha_inicio_contrato date NOT NULL,
  fecha_fin_contrato date, fecha_finalizacion date, fecha_renovacion date,
  plazo int,
  precio numeric(15,2) NOT NULL,
  monto_total_operacion numeric(15,2) NOT NULL,
  monto_total_letras text,
  garantia numeric(15,2) NOT NULL DEFAULT 0,
  estado varchar(20) NOT NULL DEFAULT 'VIGENTE' CHECK (estado IN ('VIGENTE','FINALIZADO')),
  empresa bigint NOT NULL REFERENCES persona_juridica,
  sucursal bigint NOT NULL REFERENCES sucursal,
  moneda bigint NOT NULL REFERENCES moneda,
  tipo_contrato_lista varchar(40) DEFAULT 'TIPOS_CONTRATOS', tipo_contrato_codigo varchar(40),
  tipo_financiacion_lista varchar(40) DEFAULT 'TIPOS_FINANCIACIONES', tipo_financiacion_codigo varchar(40),
  condicion_operacion varchar(10) NOT NULL CHECK (condicion_operacion IN ('CONTADO','CREDITO')),
  aviso_renovacion boolean NOT NULL DEFAULT false,
  dia_pago smallint,
  monto_mora numeric(15,2) NOT NULL DEFAULT 0,
  dias_gracia smallint,
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  FOREIGN KEY (tipo_contrato_lista, tipo_contrato_codigo) REFERENCES entidad,
  FOREIGN KEY (tipo_financiacion_lista, tipo_financiacion_codigo) REFERENCES entidad
);
CREATE INDEX ix_operacion_estado ON operacion (estado, empresa);

CREATE TABLE rescision (
  rescision bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  operacion bigint NOT NULL REFERENCES operacion,
  fecha date NOT NULL,
  tipo varchar(30),
  monto numeric(15,2) NOT NULL DEFAULT 0,
  observacion text,
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz
);

-- ── Dinero: documento + detalle + cuotas + cobros (patron Gestion) ─────────

CREATE TABLE rango_comprobante (
  rango_comprobante bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  empresa bigint NOT NULL REFERENCES persona_juridica,
  tipo_lista varchar(40) NOT NULL DEFAULT 'TIPOS_DOCUMENTO',
  tipo_codigo varchar(40) NOT NULL,
  serie varchar(20) NOT NULL,
  numero_desde bigint NOT NULL,
  numero_actual bigint NOT NULL,
  numero_hasta bigint NOT NULL,
  fecha_desde date, fecha_vencimiento date,
  timbrado varchar(25),
  estado varchar(10) NOT NULL DEFAULT 'ACTIVO' CHECK (estado IN ('ACTIVO','INACTIVO')),
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  FOREIGN KEY (tipo_lista, tipo_codigo) REFERENCES entidad,
  UNIQUE (empresa, tipo_codigo, serie, numero_desde)
);

CREATE TABLE documento (
  documento bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  empresa bigint NOT NULL REFERENCES persona_juridica,
  tipo_lista varchar(40) NOT NULL DEFAULT 'TIPOS_DOCUMENTO',
  tipo_codigo varchar(40) NOT NULL,
  serie varchar(20) NOT NULL,
  numero bigint NOT NULL,
  fecha date NOT NULL,
  fecha_vencimiento date,
  persona bigint REFERENCES persona,
  sucursal bigint REFERENCES sucursal,
  moneda bigint NOT NULL REFERENCES moneda,
  cotizacion numeric(15,5) NOT NULL DEFAULT 1,
  total numeric(15,2) NOT NULL DEFAULT 0,
  saldo numeric(15,2) NOT NULL DEFAULT 0,
  direccion_dinero varchar(10) NOT NULL CHECK (direccion_dinero IN ('ENTRADA','SALIDA')),
  estado varchar(20) NOT NULL DEFAULT 'PENDIENTE' CHECK (estado IN ('PENDIENTE','CANCELADO','ANULADO')),
  timbrado varchar(25),
  documento_ref bigint REFERENCES documento,
  observacion varchar(250),
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  FOREIGN KEY (tipo_lista, tipo_codigo) REFERENCES entidad,
  UNIQUE (empresa, tipo_codigo, serie, numero),
  CHECK (saldo >= 0 AND saldo <= total)
);

CREATE TABLE documento_detalle (
  documento_detalle bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  documento bigint NOT NULL REFERENCES documento ON DELETE CASCADE,
  numero_item int NOT NULL,
  concepto varchar(250) NOT NULL,
  articulo bigint REFERENCES articulo,
  cantidad numeric(15,3) NOT NULL DEFAULT 1,
  precio_unitario numeric(15,2) NOT NULL DEFAULT 0,
  exentas numeric(15,2) NOT NULL DEFAULT 0,
  gravadas numeric(15,2) NOT NULL DEFAULT 0,
  porcentaje_impuesto numeric(5,2) NOT NULL DEFAULT 0,   -- snapshot del impuesto del articulo al facturar
  impuestos numeric(15,2) NOT NULL DEFAULT 0,
  monto numeric(15,2) NOT NULL,
  saldo numeric(15,2) NOT NULL DEFAULT 0,
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,
  usuario_modificacion varchar(20), fecha_modificacion timestamptz,
  UNIQUE (documento, numero_item)
);

CREATE TABLE cronograma_cuota (
  cronograma_cuota bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  operacion bigint NOT NULL REFERENCES operacion,
  documento bigint REFERENCES documento,
  numero_cuota int NOT NULL,
  fecha_vencimiento date NOT NULL,
  monto numeric(15,2) NOT NULL,
  saldo numeric(15,2) NOT NULL DEFAULT 0,
  estado varchar(20) NOT NULL DEFAULT 'PENDIENTE' CHECK (estado IN ('PENDIENTE','CANCELADO')),
  moneda bigint NOT NULL REFERENCES moneda,
  fecha_cancelacion date,
  usuario_creacion varchar(20) NOT NULL, fecha_creacion timestamptz NOT NULL,

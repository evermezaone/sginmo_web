-- ── Vista unificadora de personas [Obs 7: decision del usuario] ────────────
-- Equivalente del v_ente de Gestion: una sola consulta con los datos de la persona
-- y su especializacion (fisica o juridica), para grillas, buscadores y reportes.

CREATE VIEW v_persona AS
SELECT p.persona,
       p.tipo_personeria,
       p.nombre,
       p.numero_documento,
       p.digito_verificador,
       p.tipo_documento_codigo,
       p.es_contribuyente,
       p.clasificacion_fiscal,
       p.direccion, p.telefono, p.email,
       p.ubicacion, p.ubicacion_url,
       p.estado,
       pf.nombres, pf.apellidos, pf.sexo, pf.fecha_nacimiento,
       pf.estado_civil_codigo, pf.nacionalidad,
       pj.razon_social, pj.nombre_fantasia, pj.fecha_constitucion,
       pj.representante_legal, pj.actividad_codigo
FROM persona p
LEFT JOIN persona_fisica  pf ON pf.persona = p.persona
LEFT JOIN persona_juridica pj ON pj.persona = p.persona;
